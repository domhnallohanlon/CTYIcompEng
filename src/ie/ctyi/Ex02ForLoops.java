package ie.ctyi;

public class Ex02ForLoops {
	
	public static void main(String[] args) {
		
	
	/*
	 * The steps for creating a FOR loop are:
	 * 1. Initialise the loop counter
	 * 2. Set the condition for terminating the loop
	 * 3. Increment (increase) the loop
	 */
	for(int i = 1; i<101; i++){			
		System.out.println(i+ ": If at first you don't succeed, try, try again!"); 	//this "+" character here is for concatenation, i.e. putting multiple pieces of data together
	}
	}
}
